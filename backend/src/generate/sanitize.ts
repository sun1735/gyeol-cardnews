// 과장/의학적 단정 표현을 완화해 신뢰감 있는 톤으로 치환한다.
// 자동 생성(프롬프트)·수동 입력(카드별) 양쪽에서 공통 적용된다.
// 단계 4: 임산부/영유아/시니어 케어 맥락에서 자주 등장하는 표현을 추가 필터링.

const REPLACERS: Array<[RegExp, string]> = [
  // ── 의학적 단정
  [/완치|완벽히\s?낫/g, '도움이 되는'],
  [/100\s?%|백\s?퍼센트/g, '충분히'],
  [/부작용\s?(없|제로)/g, '안전에 유의한'],
  [/기적(의|적인)?/g, '의미 있는'],
  [/보장(합니|드립|됩)/g, '도와드립'],
  [/확실히\s?(나|낫|치료)/g, '꾸준히 관리'],
  [/즉시\s?효과/g, '꾸준한 변화'],
  [/반드시\s?(낫|해결|완화)/g, '꾸준히 돕'],
  [/의학적으로\s?(증명|입증)/g, '전문가 의견을 참고한'],
  [/임상적으로\s?(증명|입증)/g, '근거 있는'],

  // ── 과장/절대 표현
  [/(업계\s?)?1\s?위/g, '신뢰받는'],
  [/전국\s?최고|세계\s?최고/g, '믿을 수 있는'],
  [/최고의\s?(효과|품질|서비스)/g, '믿을 수 있는 $1'],
  [/무조건\s?(안전|효과|해결)/g, '세심하게 $1'],
  [/유일한\s?(해결|치료|방법)/g, '저희만의 $1'],
]

export function sanitizeText(input: string): string {
  if (!input) return input
  let out = input
  for (const [re, rep] of REPLACERS) out = out.replace(re, rep)
  return out.trim()
}
